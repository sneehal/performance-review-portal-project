-- ============================================================
-- Oracle Sequences (Auto-increment IDs)
-- Oracle doesn't have AUTO_INCREMENT like MySQL
-- We use SEQUENCES + TRIGGERS to auto-generate IDs
-- ============================================================

-- Drop sequences if exist
BEGIN EXECUTE IMMEDIATE 'DROP SEQUENCE seq_users';          EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP SEQUENCE seq_cycles';         EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP SEQUENCE seq_goals';          EXCEPTION WHEN OTHERS THEN NULL; END;
